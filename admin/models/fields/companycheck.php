// import the list field type
jimport('joomla.form.helper');
JFormHelper::loadFieldClass('checkboxes');

/**
 * Companycheck Form Field class for the Costbenefitprojection component
 */
class JFormFieldCompanycheck extends JFormFieldCheckboxes
{
	/**
	 * The companycheck field type.
	 *
	 * @var		string
	 */
	public $type = 'companycheck';

	/**
	 * Method to get a list of options for a list input.
	 *
	 * @return	array    An array of JHtml options.
	 */
	protected function getOptions()
	{
		// Get the user object.
		$user = JFactory::getUser();
		// Create a new query object.
		$db = JFactory::getDBO();
		$query = $db->getQuery(true);
		$query->select($db->quoteName(array('a.id','a.name'),array('id','testcompanies_name')));
		$query->from($db->quoteName('#__costbenefitprojection_company', 'a'));
		$query->where($db->quoteName('a.published') . ' = 1');
		if (!$user->authorise('core.options', 'com_costbenefitprojection'))
		{
			$companies = CostbenefitprojectionHelper::hisCompanies($user->id);
			if (CostbenefitprojectionHelper::checkArray($companies))
			{
				$companies = implode(',',$companies);
				// only load this users companies
				$query->where('a.id IN (' . $companies . ')');
			}
			else
			{
				// dont allow user to see any companies
				$query->where('a.id = -4');
			}
		}
		$query->order('a.name ASC');
		$db->setQuery((string)$query);
		$items = $db->loadObjectList();
		$options = array();
		if ($items)
		{
			foreach($items as $item)
			{
					$tmp = array(
						'value'    => $item->id,
						'text'     => '&nbsp;<strong>'.$item->testcompanies_name.'</strong>',
						'checked'  => false
					);
					$options[] = (object) $tmp;
			}
		}
		return $options;
	}
}
